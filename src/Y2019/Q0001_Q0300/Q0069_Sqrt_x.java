package Y2019.Q0001_Q0300;

public class Q0069_Sqrt_x {

    /*
     *------------------------------------------------------------------------------------------------------------------
     *
     * solution 1: use long to handle overflow
     *
     *------------------------------------------------------------------------------------------------------------------
     */
    public int mySqrt_long(int x) {
        if (x < 2) return x;
        long l = 0, r = x / 2;
        while (l < r) {
            long m = r - (r - l) / 2;
            if (m * m > x) {
                r = m - 1;
            } else {
                l = m;
            }
        }
        return (int) l;
    }

    /*
     *------------------------------------------------------------------------------------------------------------------
     *
     * solution 2: use divide to handle overflow
     *
     *------------------------------------------------------------------------------------------------------------------
     */
    public int mySqrt(int x) {
        if (x < 2) return x;
        int l = 0, r = x / 2;
        while (l < r) {
            int m = r - (r - l) / 2;
            if (x / m < m) {
                r = m - 1;
            } else {
                l = m;
            }
        }
        return l;
    }


}
